package com.mfzn.deepuses.model.xiangmu;

public class EnginerListModel {

    private int userID;
    private int engineerID;
    private String remark;
    private int addTime;
    private String engineerName;
    private String engineerPhone;
    private String engineerAvatar;
    private int data_id;

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public int getEngineerID() {
        return engineerID;
    }

    public void setEngineerID(int engineerID) {
        this.engineerID = engineerID;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public int getAddTime() {
        return addTime;
    }

    public void setAddTime(int addTime) {
        this.addTime = addTime;
    }

    public String getEngineerName() {
        return engineerName;
    }

    public void setEngineerName(String engineerName) {
        this.engineerName = engineerName;
    }

    public int getData_id() {
        return data_id;
    }

    public void setData_id(int data_id) {
        this.data_id = data_id;
    }

    public String getEngineerPhone() {
        return engineerPhone;
    }

    public void setEngineerPhone(String engineerPhone) {
        this.engineerPhone = engineerPhone;
    }

    public String getEngineerAvatar() {
        return engineerAvatar;
    }

    public void setEngineerAvatar(String engineerAvatar) {
        this.engineerAvatar = engineerAvatar;
    }
}
